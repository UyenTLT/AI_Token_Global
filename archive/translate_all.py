# ── Run translations ─────────────────────────────────────────────────────────

os.makedirs('es', exist_ok=True)

all_files = list(FILES.keys()) + list(REMAINING_TITLES.keys())
# deduplicate while preserving order
seen = set()
unique_files = []
for f in all_files:
    if f not in seen:
        seen.add(f)
        unique_files.append(f)

for fname in unique_files:
    src = os.path.join('en', fname)
    dst = os.path.join('es', fname)
    if not os.path.exists(src):
        print(f'SKIP (not found): {src}')
        continue

    with open(src, 'r', encoding='utf-8') as f:
        html = f.read()

    # 1. Apply common replacements
    for old, new in COMMON:
        html = html.replace(old, new)

    # 2. Apply broad content replacements
    for old, new in BROAD_CONTENT:
        html = html.replace(old, new)

    # 3. Apply per-file replacements
    if fname in FILES:
        for old, new in FILES[fname]:
            html = html.replace(old, new)

    # 4. Apply title fix for remaining files
    if fname in REMAINING_TITLES:
        old_t, new_t = REMAINING_TITLES[fname]
        if old_t != '<title>':  # skip no-op sentinel
            html = html.replace(old_t, new_t)

    with open(dst, 'w', encoding='utf-8') as f:
        f.write(html)

    print(f'OK: {dst}')

print('\nAll done.')
